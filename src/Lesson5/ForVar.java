package Lesson5;

public class ForVar {
    public static void main(String[] args) {

        int sum = 0;
        int fact = 1;

        for (int i = 1; i <= 5; i++) {
            sum += 1;
            fact *= 1;
        }
        System.out.println("сумма = " +sum);
        System.out.println("сумма = " +fact);

    }
}
